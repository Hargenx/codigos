package A08_ARA0075_PPS_NA.aula04.FactoryMethod;

public class exemplo {
    private String lala;
    private double poli;


    public exemplo(String lala, double poli) {
        this.lala = lala;
        this.poli = poli;
    }

    public String getLala() {
        return this.lala;
    }

    public void setLala(String lala) {
        this.lala = lala;
    }

    public double getPoli() {
        return this.poli;
    }

    public void setPoli(double poli) {
        this.poli = poli;
    }


}
